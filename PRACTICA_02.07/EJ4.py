def media_numeros(lista):
    """Función que calcula el área de un círculo
    Parámetros:

        - cantidad: La cantidad de números introducidos por el usuario de los que se va a calcular la media.
    Salida:
        La media de los números introducidos por el usuario.
    """
    cantidad = sum(lista)
    media = cantidad / len(lista)
    return media






